function  out = iseven(in)
%------------------------------------------------------------------------
% out = iseven(in)
%------------------------------------------------------------------------
% 
% 
% 
%------------------------------------------------------------------------
% Input Arguments:
% 	in		input integer
% 
% Output Arguments:
% 	out	1 if in value is even, 0 if not
%
%------------------------------------------------------------------------
% See also: isodd
%------------------------------------------------------------------------

%------------------------------------------------------------------------
% Sharad J. Shanbhag
% sshanbhag@neoucom.edu
%------------------------------------------------------------------------
% Created: 29 September, 2010 (SJS)
%
% Revisions:
%------------------------------------------------------------------------


%----------------------------------------------------------------
% Checks
%----------------------------------------------------------------
if ~isnumeric(in)
	error('%s; input must be numeric!', mfilename);
end

% compute modulus of input with 2
in_mod2 = mod(in, 2);

% check is remainder is 0
out = in_mod2 == 0;

