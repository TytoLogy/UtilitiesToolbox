function enable_ui(handle)
%------------------------------------------------------------------------
% enable_ui(handle)
%------------------------------------------------------------------------
% 
% sets Enable property of uicontrol object handle to on
% 
%------------------------------------------------------------------------
% Input Arguments:
% 	handle	ui control handle
%
%------------------------------------------------------------------------
% See also: disable_ui, UIutilities Toolbox
%------------------------------------------------------------------------

%------------------------------------------------------------------------
%  Sharad Shanbhag
%	sshanbha@aecom.yu.edu
%------------------------------------------------------------------------
% Created: ??? (SJS)
%
% Revisions:
%	6 October, 2009:	added help + documentation
%------------------------------------------------------------------------

set(handle, 'Enable', 'on');
