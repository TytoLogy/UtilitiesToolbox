function samples = ms2samples(ms, Fs)
%--------------------------------------------------------------------------
% samples = ms2samples(ms, Fs)
%--------------------------------------------------------------------------
%
% returns corresponding sample bin(s) for time ms and sample rate Fs
%
%--------------------------------------------------------------------------
% 	Input Arguments:
%		ms			time in milliseconds
%		Fs			sampling rate in samples/second
%--------------------------------------------------------------------------
%	Output Arguments:
% 		samples		# samples
%--------------------------------------------------------------------------
% same as sometimes used ms2bin()
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
% Sharad J. Shanbhag
% sshanbha@aecom.yu.edu
%--------------------------------------------------------------------------
% Revision History
%	
%--------------------------------------------------------------------------


samples = floor(ms * 0.001 * Fs);