function bin = ms2bin(ms, Fs)
%--------------------------------------------------------------------------
% bin = ms2bin(ms, Fs)
%--------------------------------------------------------------------------
% Utilities Toolbox
%--------------------------------------------------------------------------
% 
% returns corresponding bin(s) for time ms and sample rate Fs
%
%--------------------------------------------------------------------------
% 	Input Arguments:
%		ms			time in milliseconds
%		Fs			sampling rate in samples/second
%--------------------------------------------------------------------------
%	Output Arguments:
% 		bin		bin #
%--------------------------------------------------------------------------
% same as sometimes used ms2samples()
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
% Sharad J. Shanbhag
% sharad.shanbhag@einstein.yu.edu
%--------------------------------------------------------------------------
% Created: ??????????????
% 
% Revision History
%	21 February, 2010 (SJS): updated documentation
%--------------------------------------------------------------------------

bin = floor( 0.001 * Fs .* ms);